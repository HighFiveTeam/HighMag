    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        if(!tomodify.getText().equals("")){
            Rubrique = Rubriques.List_rubrique_by_id(Integer.parseInt(modif.getText().toString()));
            Rubrique.setRubric_title(tomodify.getText());
            Rubriques.update_rubric(Rubrique);
            DefaultListModel titlesListModel;
            DefaultListModel idsListModel;
            titlesListModel = Rubriques.getTitles();
            idsListModel = Rubriques.getIds();
            menu.setModel(titlesListModel);
            menu.validate();
            ids.setModel(idsListModel);
            ids.validate();
            if(menu.isSelectionEmpty()){ajouter.setEnabled(false);}
            tomodify.setText("");
            modif.setText("");
        }
    }//GEN-LAST:event_modifierActionPerformed

