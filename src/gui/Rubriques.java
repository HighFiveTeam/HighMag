    private void menuValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_menuValueChanged
        if(!menu.isSelectionEmpty()){
            int id = Integer.parseInt(ids.getModel().getElementAt(menu.getSelectedIndex()).toString());
            Rubrique = Rubriques.List_rubrique_by_id(id);
            modif = new JTextField();
            modif.setText(Rubrique.getRubric_id()+"");
            tomodify.setText(Rubrique.getRubric_title());
            if(Rubrique.getRubric_is_main() == 1 || id == -1){
                ajouter.setEnabled(true);
            }
            else{
                ajouter.setEnabled(false);
            }
        }
    }//GEN-LAST:event_menuValueChanged

    private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed
        if(!rubrique.getText().equals("")){
            Rubrique.setRubric_title(rubrique.getText());
            Rubrique.setRubric_main_id(Integer.parseInt(ids.getModel().getElementAt(menu.getSelectedIndex()).toString()));
            Rubriques.Ajouter_Rubrique(Rubrique);
            DefaultListModel titlesListModel;
            DefaultListModel idsListModel;

            titlesListModel = Rubriques.getTitles();
            idsListModel = Rubriques.getIds();
            menu.setModel(titlesListModel);
            menu.validate();
            ids.setModel(idsListModel);
            ids.validate();
            if(menu.isSelectionEmpty()){ajouter.setEnabled(false);}
            rubrique.setText("");

        }
    }//GEN-LAST:event_ajouterActionPerformed

